def split_domain(domain):
    parts = domain.split(".")

    # Supports domains with SLD such as .co.uk
    domain_parts = 2
    if parts[-1] in _sld and len(parts) >= 2 and parts[-2] in _sld[parts[-1]]:
        domain_parts = 3

    return DomainParts(
        domain=".".join(parts[-domain_parts:]),
        subdomain=".".join(parts[:-domain_parts]) or None,
    )
